function buildMaps(pairs: string): { s2t: Map<string, string>; t2s: Map<string, string> } {
  const s2t = new Map<string, string>();
  const t2s = new Map<string, string>();
  for (let i = 0; i < pairs.length; i += 2) {
    const s = pairs[i];
    const t = pairs[i + 1];
    if (s && t && s !== t) {
      s2t.set(s, t);
      t2s.set(t, s);
    }
  }
  return { s2t, t2s };
}

const { s2t, t2s } = buildMaps(S2T_PAIRS + S2T_EXTRA);

export function toTraditional(text: string): string {
  return Array.from(text)
    .map((c) => s2t.get(c) ?? c)
    .join("");
}

export function toSimplified(text: string): string {
  return Array.from(text)
    .map((c) => t2s.get(c) ?? c)
    .join("");
}
